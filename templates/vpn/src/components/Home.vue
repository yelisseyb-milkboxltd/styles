<script>
    export default {
        mounted() {
            /* eslint-disable no-unused-vars */
            var multiItemSlider = (function () {
                return function () {
                    var
                        _mainElement = document.querySelector('.slider-c'), // основный элемент блока
                        _sliderWrapper = _mainElement.querySelector('.slider-c__wrapper'), // обертка для .slider-item
                        _sliderItems = _mainElement.querySelectorAll('.slider-c__item'), // элементы (.slider-item)
                        _wrapperWidth = parseFloat(getComputedStyle(_sliderWrapper).width), // ширина обёртки
                        _itemWidth = parseFloat(getComputedStyle(_sliderItems[0]).width), // ширина одного элемента
                        _positionLeftItem = 0, // позиция левого активного элемента
                        _transform = 0, // значение транфсофрмации .slider_wrapper
                        _step = _itemWidth / _wrapperWidth * 100, // величина шага (для трансформации)
                        _items = []; // массив элементов
                    _sliderItems.forEach(function (item, index) {
                        _items.push({ item: item, position: index, transform: 0 });
                    });
                    var position = {
                        getItemMin: function () {
                            var indexItem = 0;
                            _items.forEach(function (item, index) {
                                if (item.position < _items[indexItem].position) {
                                    indexItem = index;
                                }
                            });
                            return indexItem;
                        },
                        getItemMax: function () {
                            var indexItem = 0;
                            _items.forEach(function (item, index) {
                                if (item.position > _items[indexItem].position) {
                                    indexItem = index;
                                }
                            });
                            return indexItem;
                        },
                        getMin: function () {
                            return _items[position.getItemMin()].position;
                        },
                        getMax: function () {
                            return _items[position.getItemMax()].position;
                        }
                    }
                    setInterval(function () {
                        var nextItem;
                        _positionLeftItem++;
                        if ((_positionLeftItem + _wrapperWidth / _itemWidth - 1) > position.getMax()) {
                            nextItem = position.getItemMin();
                            _items[nextItem].position = position.getMax() + 1;
                            _items[nextItem].transform += _items.length * 100;
                            _items[nextItem].item.style.transform = 'translateX(' + _items[nextItem].transform + '%)';
                        }
                        _transform -= _step;
                        _sliderWrapper.style.transform = 'translateX(' + _transform + '%)';
                    }, 3000)
                }
            }());
            multiItemSlider()
        },
    }
</script>